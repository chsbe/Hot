package be.icode.hot.cli;

public class DataSourceAlreadyDefinedException extends Exception {

	public DataSourceAlreadyDefinedException() {
	}

	public DataSourceAlreadyDefinedException(String arg0) {
		super(arg0);
	}

	public DataSourceAlreadyDefinedException(Throwable arg0) {
		super(arg0);
	}

	public DataSourceAlreadyDefinedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
