package be.solidx.hot.data.criterion;

public class CriterionCreationException extends RuntimeException {

	private static final long serialVersionUID = -8830054162901120161L;

	public CriterionCreationException() {
		super();
	}

	public CriterionCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CriterionCreationException(String arg0) {
		super(arg0);
	}

	public CriterionCreationException(Throwable arg0) {
		super(arg0);
	}
}
